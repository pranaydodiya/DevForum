
import React, { createContext, useContext, useState, useEffect, ReactNode } from 'react';

export interface Badge {
  id: string;
  name: string;
  description: string;
  icon: string;
  category: 'milestone' | 'language' | 'activity' | 'special';
  requirement: number;
  earnedAt?: string;
}

export interface SkillTag {
  tag: string;
  level: 'beginner' | 'intermediate' | 'advanced' | 'expert';
  endorsements: number;
  codeContributions: number;
  autoGenerated: boolean;
}

export interface ReputationActivity {
  type: 'upvote' | 'accepted_answer' | 'helpful_comment' | 'streak' | 'rare_topic' | 'challenge_win';
  points: number;
  timestamp: string;
  description: string;
}

export interface Challenge {
  id: string;
  title: string;
  description: string;
  difficulty: 'easy' | 'medium' | 'hard';
  language: string;
  startDate: string;
  endDate: string;
  submissions: ChallengeSubmission[];
  isActive: boolean;
}

export interface ChallengeSubmission {
  id: string;
  challengeId: string;
  userId: string;
  userName: string;
  code: string;
  votes: number;
  submittedAt: string;
  performance: number;
}

interface EngagementContextType {
  reputation: number;
  badges: Badge[];
  skillTags: SkillTag[];
  reputationHistory: ReputationActivity[];
  challenges: Challenge[];
  leaderboard: Array<{
    userId: string;
    userName: string;
    reputation: number;
    badges: number;
    weeklyPoints: number;
  }>;
  earnBadge: (badgeId: string) => void;
  addReputationPoints: (activity: Omit<ReputationActivity, 'timestamp'>) => void;
  endorseSkill: (skillTag: string) => void;
  submitChallenge: (challengeId: string, code: string) => void;
  voteOnSubmission: (submissionId: string, vote: 'up' | 'down') => void;
}

const EngagementContext = createContext<EngagementContextType | undefined>(undefined);

export const useEngagement = () => {
  const context = useContext(EngagementContext);
  if (context === undefined) {
    throw new Error('useEngagement must be used within an EngagementProvider');
  }
  return context;
};

